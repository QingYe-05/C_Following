/*
C语言中的math.h头文件提供了许多常用的数学函数。下面是一些常见的math.h函数：

数值操作：

abs()：求取整型或浮点型数的绝对值。
fabs()：求取浮点型数的绝对值。
ceil()：向上取整，返回不小于给定浮点型数的最小整数值。
floor()：向下取整，返回不大于给定浮点型数的最大整数值。
round()：四舍五入，返回给定浮点型数最接近的整数值。
fmod()：浮点型数求模运算，返回两个浮点型数相除的余数。
三角函数：

sin()：求正弦值。
cos()：求余弦值。
tan()：求正切值。
asin()：求反正弦值。
acos()：求反余弦值。
atan()：求反正切值。
指数与对数：

pow()：求幂运算，返回给定底数的给定指数次幂。
sqrt()：求平方根。
exp()：求指数函数，返回e的给定指数次幂。
log()：求以e为底的自然对数。
log10()：求以10为底的对数。
其他常用函数：

max()：返回两个参数中较大的一个。
min()：返回两个参数中较小的一个。
rand()：生成随机数。
srand()：设置随机数生成的种子值。
*/